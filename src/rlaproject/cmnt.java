    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try
        {   
            int d1_len=t1.getText().length();
            int d2_len=t2.getText().length();
            if(d1_len==0 || d2_len==0)
            {
                JOptionPane.showMessageDialog(rootPane, "Please Fill Required Fields to Submit");
                if(d1_len==0)
                {
                JOptionPane.showMessageDialog(rootPane, "Please insert your Name");
                } 
                else if(d2_len==0)
                {
                JOptionPane.showMessageDialog(rootPane, "Please insert your Designation");
                }
            }  
             else
            {    
            int res=JOptionPane.showConfirmDialog(null,"Want to submit","Info",JOptionPane.OK_OPTION,JOptionPane.INFORMATION_MESSAGE);
            if(res==JOptionPane.OK_OPTION)
                {
                    PreparedStatement pstmt=con.prepareStatement("insert into Feedback values(?,?,?,?)");
                    pstmt.setString(1,t1.getText());
                    pstmt.setString(2,t2.getText());
                    pstmt.setString(3,(comment.getText()));
                    java.util.Date d1=new java.util.Date();
                    String newdate=new SimpleDateFormat("dd/MM/yyyy").format(d1);
                    pstmt.setString(4,newdate); 
                    pstmt.executeUpdate();
                    JOptionPane.showMessageDialog(null,"Thank you!!! for your valueable Suggestions ");
                }
            }
        }
        catch(Exception obj)
        {
            System.out.println(obj.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

